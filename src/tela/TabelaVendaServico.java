    private String convertCalendarInString(Calendar data){
        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
        return sdf.format(data.getTime());

    }
    
    private void preencheTabela(){
        BdVendaServico bdvs = new BdVendaServico();
        DefaultTableModel modelo = (DefaultTableModel) tVendas.getModel();
        int i = modelo.getRowCount();
        while(i-->0){
            modelo.removeRow(i);
        }
        ArrayList v = bdvs.pesquisa();
        for(Iterator it = v.iterator(); it.hasNext();){
            VendaServico vs = (VendaServico) it.next();
            BdClientes bdc = new BdClientes();
            BdAnimal bda = new BdAnimal();
            Animal a = bda.localiza(vs.getCodigoAnimal());
            Cliente c = bdc.localiza(vs.getCodigocliente());
            modelo.addRow(new Object[]{vs.getCodigo(), a.getNome(), c.getNome(), vs.getTotal(), convertCalendarInString(vs.getData()) , vs.getHora()});
            bda.fecha();
            bdc.fecha();
        }
        
            bdvs.fecha();
            
        
    }
    
    
    

