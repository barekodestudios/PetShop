    private void tVendasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tVendasMouseClicked
        if(evt.getClickCount() == 2){
        Point p = evt.getPoint();
        int row = tVendas.rowAtPoint(p);
        DefaultTableModel modelo = (DefaultTableModel) tVendas.getModel();
        int codigo = (Integer) modelo.getValueAt(row, 0);
        BdVendaServico bdvs = new BdVendaServico();
        TelaVendaServicos t = new TelaVendaServicos();
        t.setVendas(bdvs.localizaCodigo(codigo));
        t.setNovo(false);
        t.setVisible(true);
        }
    }//GEN-LAST:event_tVendasMouseClicked

