    private void tLancMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tLancMouseReleased
        if(evt.getClickCount() == 2){
            Point p = evt.getPoint();
            int row = tLanc.rowAtPoint(p);
            int col = tLanc.columnAtPoint(p);
            DefaultTableModel modelo = (DefaultTableModel) tLanc.getModel();
            int codigo = (Integer) modelo.getValueAt(row, 0);
            CadastroTipoLancamento t = new CadastroTipoLancamento();
            t.setNovo(false);
            t.setTl(bdtf.localizaByCodigo(codigo));
            t.setVisible(true);
        }
    }//GEN-LAST:event_tLancMouseReleased

