    private void preencheTabela(){
        DefaultTableModel modelo = (DefaultTableModel) tLanc.getModel();
        int i = modelo.getRowCount();
        while(i-- > 0){
            modelo.removeRow(i);
        }
        ArrayList c = bdtf.pesquisaAll(tBusca.getText());
        for(Iterator it = c.iterator(); it.hasNext();){
            TipoLancamento tl = (TipoLancamento) it.next();
            modelo.addRow(new Object[]{tl.getCodigo(), tl.getLancamento(), tl.getEs()});
        }
    }

