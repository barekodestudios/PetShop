    private void telaToFinanceiro(){
        BdTipoFinanceiro bdtf = new BdTipoFinanceiro();
        TipoLancamento tl = new TipoLancamento();
        tl = bdtf.localiza((String) cmbMPag.getSelectedItem());
        finc.setTipo(tl.getCodigo());
        finc.setEs(tl.getEs());
        finc.setValor(Double.parseDouble(tValor.getText()));
        finc.setData(getData());
        finc.setHora(getHora());
    }
